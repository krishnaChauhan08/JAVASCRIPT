let fruit = "banana";
switch (fruit) {
  case "banana":
    console.log("Banana is good!");
    break;

  case "orange":
    console.log("I am not fan of orange.");
    break;

  case "apple":
    console.log("How you like them apples?");
    break;

  default:
    console.log("I have never heard of that fruit.");
    break;
}
